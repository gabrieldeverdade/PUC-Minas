/*
p++ : Depois que esta intrução é executada, o ponteiro (p) aponta
para a posição de memória imediatamente superior. Se estiver em um
vetor, o ponteiro aponta para a próxima posição do vetor.

(*p)++ : Ele incrementa o conteúdo que é apontado por p, ou seja, o valor
é armazenado na variável na qual p aponta.

*(p++) : Assim como em p++, ele incrementa p e acessa o valor que é
encontrado na nova posição. Porém se está em um vetor, essa expressão
acessa o valor da posição imediatamente superior armazenada em p antes
do incremento
  */